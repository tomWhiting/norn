    #[serial]
    fn existing_default_file_is_not_overwritten() -> Result<(), Box<dyn Error>> {
        let dir = tempdir()?;
        let path = dir.path().join(DEFAULT_FILENAME);
        let sentinel = "# do not touch\n";
        fs::write(&path, sentinel)?;

        let code = with_cwd(dir.path(), || run_conventions(None))?;
        assert_eq!(code, ExitCode::Success);
        assert_eq!(fs::read_to_string(&path)?, sentinel);
        Ok(())
    }

