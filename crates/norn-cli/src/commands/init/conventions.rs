    // -- R7: safe write flow --

    /// Save the current directory, switch into `dir`, run `f`, then restore.
    fn with_cwd<R>(dir: &Path, f: impl FnOnce() -> R) -> io::Result<R> {
        let original = std::env::current_dir()?;
        std::env::set_current_dir(dir)?;
        let result = f();
        std::env::set_current_dir(original)?;
        Ok(result)
    }

